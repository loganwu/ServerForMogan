CREATE TABLE IF NOT EXISTS HeroInfo (
		 HeroId INT NOT NULL   ,
		 FamousHeroId INT   ,
		 AttrMask TINYINT   ,
		 HeroName VARCHAR(19)   ,
		 Experience INT   ,
		 RoleId INT   ,
		 AttackGPRate TINYINT   ,
		 DefenceGPRate TINYINT   ,
		 BrainpowerGPRate TINYINT   ,
		 AgilityGPRate TINYINT   ,
		 AttackCircleGP TINYINT   ,
		 DefenceCircleGP TINYINT   ,
		 BrainpowerCircleGP TINYINT   ,
		 AgilityCircleGP TINYINT   ,
		 HeroLevel TINYINT   ,
		 Sex TINYINT   ,
		 HeroClass TINYINT   ,
		 PosX TINYINT   ,
		 PosY TINYINT   ,
		 AttackStrategy TINYINT   ,
		 Attack SMALLINT   ,
		 Defence SMALLINT   ,
		 Brainpower SMALLINT   ,
		 Agility SMALLINT   ,
		 MarshalPoint SMALLINT   ,
		 Intellect SMALLINT   ,
		 LevelGP SMALLINT   ,
		 HeroImageId SMALLINT   ,
		 HeroHP SMALLINT   ,
		 InfantryAddition TINYINT   ,
		 CavalryAddition TINYINT   ,
		 ArcherAddition TINYINT   ,
		 PoliticalAddition TINYINT   ,
		 TrainingBeginTime INT   ,
		 TrainingPara INT   ,
		 TransitionFlag TINYINT   ,
		 HeroBlob BLOB   ,
		PRIMARY KEY(HeroId))
		 ENGINE=InnoDB DEFAULT CHARSET=latin1;
